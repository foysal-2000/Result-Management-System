  <script>
    function submitLogoutForm() {
        // Create a form element
        const form = document.createElement('form');
        form.method = 'POST';
        form.action = '{{ route('logout') }}'; // Replace with your logout route

        // Create a CSRF token input
        const csrfToken = document.createElement('input');
        csrfToken.type = 'hidden';
        csrfToken.name = '_token';
        csrfToken.value = '{{ csrf_token() }}';

        // Append the CSRF token input to the form
        form.appendChild(csrfToken);

        // Append the form to the body and submit it
        document.body.appendChild(form);
        form.submit();
    }
</script>



<script>
    // Fetch notifications with AJAX
    function fetchNotifications() {
        $.get('/notifications', function(data) {
            // Update the notification dropdown with fetched data
            updateNotifications(data.notifications);
        });
    }

    // Update the notification dropdown
    function updateNotifications(notifications) {
        const dropdown = $('.notifications');
        dropdown.empty();

        if (notifications.length === 0) {
            dropdown.append('<li>No new notifications</li>');
        } else {
            notifications.forEach(function(notification) {
                dropdown.append('<li>' + notification.message + '</li>');
            });
        }
    }

    // Poll for new notifications periodically (e.g., every 30 seconds)
    setInterval(fetchNotifications, 30000);

    // Initial fetch of notifications when the page loads
    $(document).ready(function() {
        fetchNotifications();
    });
</script>
